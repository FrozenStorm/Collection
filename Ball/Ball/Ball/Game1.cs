        /// </summary>
        protected override void Initialize()
        {
            Ball = Content.Load<Texture2D>("Ball_Bild");
            for (int i = 0; i < spur; i++)
            {
                position.Add(new Vector2(0, 0));
            }
            base.Initialize();
        }

